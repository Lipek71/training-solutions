package week10.d01;

public class Elevations2 {
    private int up;
    private int down;

    public int getUp() {
        return up;
    }

    public int getDown() {
        return down;
    }

    public void upInc(int value){
        up += value;
    }

    public void downInc(int value){
        down += value;
    }

}
